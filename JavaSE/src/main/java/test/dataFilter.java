    public static void filter(List<Integer> heartList, List<Integer> turnList, List<Integer> breathList,List<String> dateList) {
        int commonSize = Math.min(Math.min(heartList.size(), turnList.size()), breathList.size());
        Iterator<Integer> heartIterator = heartList.iterator();
        Iterator<Integer> turnIterator = turnList.iterator();
        Iterator<Integer> breathIterator = breathList.iterator();
        Iterator<String> dateIterator = dateList.iterator();
        for (int i = 0; i < commonSize; i++) {
            if (!heartIterator.hasNext() || !turnIterator.hasNext() || !breathIterator.hasNext()) {
                // 如果其中一个列表已经遍历完，则直接跳出循环
                break;
            }
            Integer heartValue = heartIterator.next();
            Integer turnValue = turnIterator.next();
            Integer breathValue = breathIterator.next();
            if (heartValue == 0 || turnValue == 0 || breathValue == 0) {
                // 如果任意一个值为0，则移除三个列表中的对应元素
                heartIterator.remove();
                turnIterator.remove();
                breathIterator.remove();
                dateIterator.remove();
            }
        }
    }

